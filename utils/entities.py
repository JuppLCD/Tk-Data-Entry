class User:
    def __init__(
        self,
            firstname,
            lastname,
            title,
            age,
            nationality,
            numcourses,
            numsemesters,
            registration_status
    ):
        self.firstname = firstname
        self.lastname = lastname
        self.title = title
        self.age = age
        self.nationality = nationality
        self.numcourses = numcourses
        self.numsemesters = numsemesters
        self.registration_status = registration_status
